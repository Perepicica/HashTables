package hashTable1;

public class Pair1 {
    private String key;
    private String value;

    public String getKey() {
        return this.key;
    }

    public String getValue() {
        return this.value;
    }

    public void setValue(String value) {
        this.value = value;
    }


    public Pair1(String key, String value) {
        this.key = key;
        this.value = value;
    }
}
